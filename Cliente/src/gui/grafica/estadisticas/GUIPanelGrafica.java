    public BufferedImage creaImagen() {
        XYSeries series = new XYSeries("Evolucion");
//          for (int i = 0; i < listaEjeY.size() ; i++) {
//            series.add(i, listaEjeY.get);
//        }
        series.add(4, 23);
        series.add(2, 34);
        series.add(3, 51);
        series.add(4, 67);
        series.add(5, 89);
        series.add(6, 121);
        series.add(7, 137);
        XYDataset juegoDatos = new XYSeriesCollection(series);

        JFreeChart chart = ChartFactory.createXYLineChart(titulo,
                leyendaEjeX, leyendaEjeY, juegoDatos, PlotOrientation.VERTICAL,
                false,
                false,
                true // Show legend
                );
        log.info("Valor de Width:" + getWidth());
        log.info("Valor de Heigth:" + getHeight());
        BufferedImage image = chart.createBufferedImage(getWidth(), getHeight());
        return image;
    }

