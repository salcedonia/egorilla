    @Override
    public void paint(java.awt.Graphics g) {
        //super.paint(g);
        if (buffer == null) {
            buffer = creaImagen();
        }
        float fancho = this.getAlignmentX();
        float falto = this.getAlignmentY();
        int alto = Math.round(falto);
        int ancho = Math.round(fancho);
        if (g == null) {
            // g = new Graphics();
        }
        g.drawImage(buffer, alto, ancho, null);
    }

